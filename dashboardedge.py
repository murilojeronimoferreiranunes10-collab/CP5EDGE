# ✅ CORRIGIDO: era "http//..." (faltava ':'), agora "http://..."
BASE_URL = "http://34.170.247.140"   # IP do servidor FIWARE (Orion/STH)
STH_PORT = 8666

# ✅ CORRIGIDO: era Lamp:002 — agora bate com o device_id do Postman e do .ino
ENTITY_TYPE = "Lamp"
ENTITY_ID   = "urn:ngsi-ld:Lamp:001"

FIWARE_SERVICE     = "smart"
FIWARE_SERVICEPATH = "/"

# Atributos — nome exato como registrado no IoT Agent
# object_id no .ino:  l → luminosity | u → humidity | t → temperature
ATRIBUTOS = {
    "luminosity" : {"label": "Luminosidade", "unidade": "lux", "cor": "#f39c12", "emoji": "💡"},
    "humidity"   : {"label": "Umidade",       "unidade": "%",   "cor": "#2980b9", "emoji": "💧"},
    "temperature": {"label": "Temperatura",   "unidade": "°C",  "cor": "#e74c3c", "emoji": "🌡️"},
}

